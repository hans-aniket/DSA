// This is a simple C++ program to test the .gitignore rules.
#include <iostream>
#include <vector>

int main() {
    // Declare a vector of integers
    std::vector<int> numbers = {1, 2, 3, 4, 5};

    // Print a message to the console
    std::cout << "Hello from the array directory!" << std::endl;

    // Loop through the vector and print each number
    for (int num : numbers) {
        std::cout << num << " ";
    }
    std::cout << std::endl;

    return 0;
}
